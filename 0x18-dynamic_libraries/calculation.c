#include <stdio.h>
/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two numbers
 * @a: first number
 * @b: second number
 * Return: result of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two numbers
 * @a: first number
 * @b: second number
 * Return: result of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two numbers
 * @a: first number
 * @b: second number
 * Return: result of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two numbers
 * @a: first number
 * @b: second number
 * Return: result of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
